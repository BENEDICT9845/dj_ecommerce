from django.urls import path
from django.contrib.auth.decorators import login_required
from .views import (
    ProfileListView,
    ProfileDetailView,
    ProfileCreateView,
    ProfileUpdateView,
    ExperienceCreateView,
    ExperienceListView,
    ExperienceUpdateView,
    profile_experience_delete_view,
    profile_create_education_view,
    profile_education_list_view,
    profile_education_update_view,
    profile_education_delete_view
)

app_name = 'profiles'
urlpatterns = [
    path(
        '',
        ProfileListView.as_view(),
        name='profile_list'
    ),
    path(
        'create/',
        login_required(ProfileCreateView.as_view()),
        name='profile_create'
    ),
    path(
        '<int:pk>/detail/',
        ProfileDetailView.as_view(),
        name='profile_detail'
    ),
    path(
        '<int:pk>/update/',
        login_required(ProfileUpdateView.as_view()),
        name='profile_update'
    ),
    path(
        '<int:pk>/experiences/',
        login_required(ExperienceListView.as_view()),
        name='profile_experience_list'
    ),
    path(
        '<int:pk>/educations/',
        profile_education_list_view,
        name='profile_education_list'
    ),
    path(
        '<int:pk>/experiences/create/',
        login_required(ExperienceCreateView.as_view()),
        name='profile_create_experience'
    ),
    path(
        'experiences/<int:pk>/update/',
        login_required(ExperienceUpdateView.as_view()),
        name='profile_experience_update'
    ),
    path(
        'experiences/<int:pk>/delete/',
        profile_experience_delete_view,
        name='profile_experience_delete'
    ),
    path(
        '<int:pk>/educations/create/',
        profile_create_education_view,
        name='profile_create_education'
    ),
    path(
        'educations/<int:pk>/update/',
        profile_education_update_view,
        name='profile_education_update'
    ),
    path(
        'educations/<int:pk>/delete/',
        profile_education_delete_view,
        name='profile_education_delete'
    ),
]
